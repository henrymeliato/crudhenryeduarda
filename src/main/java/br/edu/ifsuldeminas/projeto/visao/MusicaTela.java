    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        Musica musica = new Musica();
        MusicaDAO mdao = new MusicaDAO();
        PlaylistTela pTela = new PlaylistTela();

        musica.setMusicaID(Integer.parseInt(txtId.getText()));
        musica.setTitulo(txtNome.getText());
        musica.setArtista(jComboBox1.getSelectedItem().toString());
        musica.setAlbum(txtAlbum.getText());
        
        mdao.CadastrarMusica(musica);
        
        JOptionPane.showMessageDialog(null, "Música Cadastrada");

        this.dispose();
        pTela.setVisible(true);
    }//GEN-LAST:event_btnAddActionPerformed

