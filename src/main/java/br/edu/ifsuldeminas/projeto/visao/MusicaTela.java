    private void jComboBox1FocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jComboBox1FocusGained
        List<Artista> listaArtistas = new ArtistaDAO().getArtistas();


        String things[] = new String[listaArtistas.size()];
        int x = 0;
        while (x < listaArtistas.size()) {
            
            things[x] = listaArtistas.get(x).getArtistaNome();

            x++;
        }
        
        jComboBox1.setModel(new javax.swing.DefaultComboBoxModel(things));
    }//GEN-LAST:event_jComboBox1FocusGained

